public class Circle {
    public static void main(String[] args) {
       
        double r = 5.0; 

        // Declare variable for area and pi
        double a, pi;

        // Assign value to pi
        pi = 22.0 / 7;

        // Calculate the area
        a = pi * r * r;

        // Display results
        System.out.println("Radius of the circle = " + r);
        System.out.println("Area of the circle = " + a);
    }
}

